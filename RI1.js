function comparar(p1, p2) {
    console.log("\n" + "=".repeat(50));
    console.log("COMPARAÇÃO: " + p1.codinome + "  VS  " + p2.codinome);
    console.log("-".repeat(50));
    console.log(p1.descricao());
    console.log("-".repeat(50));
    console.log(p2.descricao());
    console.log("-".repeat(50));

    console.log("RESULTADO:");

    const atributos = [
        { nome: "FORÇA",       val1: p1.forca,       val2: p2.forca       },
        { nome: "VELOCIDADE",  val1: p1.velocidade,  val2: p2.velocidade  },
        { nome: "RESISTÊNCIA", val1: p1.resistencia, val2: p2.resistencia }
    ];

    atributos.forEach(attr => {
        if (attr.val1 > attr.val2) {
            console.log(`→ ${p1.codinome} vence em ${attr.nome} (${attr.val1} > ${attr.val2})`);
        } else if (attr.val2 > attr.val1) {
            console.log(`→ ${p2.codinome} vence em ${attr.nome} (${attr.val2} > ${attr.val1})`);
        } else {
            console.log(`→ Empate em ${attr.nome} (${attr.val1})`);
        }
    });

    console.log("=".repeat(50) + "\n");
}

console.log("INICIANDO COMPARAÇÕES\n");

for (let i = 0; i < personagens.length; i++) {
    for (let j = i + 1; j < personagens.length; j++) {
        comparar(personagens[i], personagens[j]);
    }
}

